// Q1
// Write a function that accepts two integers and displays
// the greater one, using `console.log`

// Define the function
function max(num1, num2) {}

function maxNumber(x1, x2) {
  return Math.max(x1, x2);
  console.log(Math.max(x1, x2));
}

// Call the function
max(12, 43);
maxNumber(12, 43);
43;
